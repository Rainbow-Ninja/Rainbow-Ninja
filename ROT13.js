function rot13(str) {
    const arrSub = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']; 
    strArr = str.split("");
    rot13Str = strArr.map( letter => {
        const lett = arrSub.indexOf(letter)
        if(lett == -1) { return letter }
        let sub = lett + 13
        if(sub > 26) { sub = sub - 26 }
        return arrSub[sub]
    })
    return rot13Str.join("")
}


